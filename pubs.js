const HIGHLIGHT_NAMES = new Set(["qi yu", "y qi"]);

document.addEventListener("DOMContentLoaded", () => {
  const tbody = document.createElement("tbody");
  const rows = PUBLICATIONS.map((data, index) => {
    const row = document.createElement("tr");
    const imageCell = createImageCell({ ...data, index });
    const contentCell = createContentCell(data);
    row.append(imageCell, contentCell);
    return row;
  });
  tbody.append(...rows);
  document.querySelector("#pub-list").append(tbody);
});

function createImageCell({ image, title, index }) {
  const cell = document.createElement("td");
  cell.style.padding = "10px";
  cell.style.width = "16%";
  cell.style.verticalAlign = "middle";

  const imageSrc =
    image ||
    "images/publications/preview-" + String(index).padStart(2, "0") + ".png";
  const frame = document.createElement("div");
  frame.className = "pub-thumb-frame";

  if (imageSrc) {
    const img = document.createElement("img");
    img.src = imageSrc;
    img.alt = title + " thumbnail";
    img.loading = "lazy";
    img.className = "pub-thumb-image";
    frame.append(img);
  } else {
    const placeholderElem = document.createElement("div");
    placeholderElem.className = "pub-thumb-placeholder";
    frame.append(placeholderElem);
  }

  cell.append(frame);
  return cell;
}

function createContentCell({ title, authors, conference, links }) {
  const cell = document.createElement("td");
  cell.style.padding = "18px 20px";
  cell.style.width = "84%";
  cell.style.verticalAlign = "middle";

  const titleElem = document.createElement("papertitle");
  titleElem.textContent = title;

  const authorsElem = createAuthorElement(authors);
  const conferenceElem = document.createElement("em");
  conferenceElem.textContent = conference;

  const linksElem = createLinksElement(links);
  const elements = [
    titleElem,
    document.createElement("br"),
    authorsElem,
    document.createElement("br"),
    conferenceElem,
    document.createElement("br"),
    linksElem,
  ].filter(Boolean);

  cell.append(...elements);
  return cell;
}

function createAuthorElement(authors) {
  if (!authors) {
    return null;
  }

  const frag = document.createDocumentFragment();
  const authorItems = authors.split(/[,;]/).map((author) => author.trim()).filter(Boolean);

  authorItems.forEach((author, index) => {
    if (HIGHLIGHT_NAMES.has(author.toLowerCase())) {
      const authorElem = document.createElement("strong");
      authorElem.textContent = author;
      frag.append(authorElem);
    } else {
      frag.append(author);
    }

    if (index !== authorItems.length - 1) {
      frag.append(", ");
    }
  });

  return frag;
}

function createLinksElement(links) {
  if (!links) {
    return null;
  }

  const frag = document.createDocumentFragment();
  const activeLinks = Object.entries(links).filter(([, link]) => link);

  activeLinks.forEach(([name, link], index) => {
    const elem = document.createElement("a");
    elem.href = link;
    elem.textContent = "[" + name + "]";
    frag.append(elem);

    if (index !== activeLinks.length - 1) {
      const spacer = document.createElement("span");
      spacer.innerHTML = "&nbsp;&nbsp;&nbsp;&nbsp;";
      frag.append(spacer);
    }
  });

  return frag;
}
